const SKIP_PATTERN = /^(\s*|[0-9\s:.,+/\-()~%°#]+|https?:\/\/\S+|\S+@\S+\.\S+)$/;

export function setActiveLanguage(code) {
  activeLanguage = code || 'en';
}

export function translateText(value, lang = activeLanguage) {
  if (lang === 'en' || typeof value !== 'string') return value;
  if (SKIP_PATTERN.test(value)) return value;

  const leading = value.match(/^\s*/)?.[0] || '';
  const trailing = value.match(/\s*$/)?.[0] || '';
  const core = value.trim();
  if (!core) return value;

  const exact = EXACT[lang]?.[core];
  if (exact) return `${leading}${exact}${trailing}`;

  let translated = core;
  for (const [from, to] of WORDS[lang] || []) {
    translated = translated.replace(new RegExp(escapeRegExp(from), 'g'), to);
  }
  return `${leading}${translated}${trailing}`;
}

export function installGlobalTextTranslator() {
  if (installed) return;
  installed = true;
  const originalCreateElement = React.createElement;

  React.createElement = function translatedCreateElement(type, props, ...children) {
    if (type === Text) {
      return originalCreateElement(type, props, ...children.map(translateNode));
    }
    if (type === TextInput && props?.placeholder) {
      return originalCreateElement(type, { ...props, placeholder: translateText(props.placeholder) }, ...children);
    }
    return originalCreateElement(type, props, ...children);
  };
}

function translateNode(node) {
  if (typeof node === 'string') return translateText(node);
  if (Array.isArray(node)) return node.map(translateNode);
  return node;
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}
